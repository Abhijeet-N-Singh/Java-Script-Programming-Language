// Booleans and comparision operators
// booleans datatypes have two values
// True or False   

let num1 = 7;
let num2 = "7";

console.log(num1<num2);
console.log(num1<=num2);
console.log(num1>num2);
console.log(num1>=num2);
// == vs ==
console.log(num1 == num2);
console.log(num1 === num2);
// = is used fro assign the values
// == is used for comparing the values between two variables.assigning
// === is used for comparision of two different datatypes
// != vs !==
console.log(num1 != num2);
console.log(num1 !== num2);
