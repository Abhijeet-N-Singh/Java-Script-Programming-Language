// the do while loop
// its a variant of the whhile loop.
// This loop will execute the code block once, before checking
// if the condition is true, then it will repeat the loop as long 
// as the condition is true.
// waArning = if yo dont increment the counter, the while loop will not stop untill forever !
 let i = 0;
 do
 {
    console.log(`current value of i is ${i}`);
    i++;
 }
 while(i<=9);

 console.log("Iterations of 'do while' loop is ended!!!")
 console.log(`current value of i after iteration is ${i}`);
