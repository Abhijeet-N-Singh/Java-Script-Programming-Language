let day=5;

switch(day)
{
    case 0:
        console.log("MOnday");
        break;
    case 1:
        console.log("Tuesday");
        break;
    case 2:
        console.log("Wednesday");
    case 3:
        console.log("Thrusday");
        break;
    case 4:
        console.log("Friday");
        break;
    case 5:
        console.log("Saturday");
        break;
    case 6:
        console.log("Sunday");
        break;
    
    default:
        console.log("Invalid Day");
}
